function isStopword(word: string, locale: string): boolean {
  if (locale.startsWith('zh')) return ZH_STOPWORDS.has(word)
  if (locale.startsWith('en')) return EN_STOPWORDS.has(word.toLowerCase())
  return false
}

// ==================== Provider ====================

interface JiebaInstance {
  tag: (text: string) => Array<{ tag: string; word: string }>
}

export function createJiebaNlpProvider(dictType: string = 'default'): NlpProvider {
  let jiebaInstance: JiebaInstance | null = null

  function getJieba(): JiebaInstance {
    if (jiebaInstance) return jiebaInstance
    try {
      // eslint-disable-next-line @typescript-eslint/no-require-imports
      const { Jieba } = require('@node-rs/jieba')
      jiebaInstance = dictType === 'default' ? new Jieba() : (Jieba.withDict?.(null) ?? new Jieba())
      return jiebaInstance!
    } catch (error) {
      throw new Error(`@node-rs/jieba not available: ${error}`)
    }
  }

  return {
    tag(text: string): PosTagResult[] {
      return getJieba().tag(text)
    },
    isStopword,
    meaningfulPosTags: MEANINGFUL_POS_TAGS,
  }
}
